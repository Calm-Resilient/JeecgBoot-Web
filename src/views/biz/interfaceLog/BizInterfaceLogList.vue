            ],
        },
        actionColumn: {
            width: 120,
            fixed: 'right'
        },
        beforeFetch: (params) => {
            if (params && fieldPickers) {
                for (let key in fieldPickers) {
                    if (params[key]) {
                        params[key] = getDateByPicker(params[key], fieldPickers[key]);
                    }
                }
            }
            return Object.assign(params, queryParam);
        },
    },
    exportConfig: {
        name: "接口日志",
        url: getExportUrl,
        params: queryParam,
    },
    importConfig: {
        url: getImportUrl,
        success: handleSuccess
